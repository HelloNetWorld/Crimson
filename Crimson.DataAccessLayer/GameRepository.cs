        private static List<Game> _games;

        public GameRepository()
        {
            if (_games == null)
                LoadDefaultData();
        }

        public IEnumerable<Game> GetAll()
        {
            return _games;
        }

        public Game GetAt(int index)
        {
            if (index < 0 && index >= _games.Count) throw new IndexOutOfRangeException(nameof(index));

            return _games[index];
        }

        public void Create(Game item)
        {
            if (item == null) throw new ArgumentNullException(nameof(item));

            _games.Add(item);
        }

        public void Update(Game item)
        {
            if (item == null) throw new ArgumentNullException(nameof(item));

            if (item.Removable == true)
            {
                // Если бы было подкл. к БД, то нужно добавить ID.
                Game gameToUpdate = _games.FirstOrDefault(g => g.Equals(item));
                gameToUpdate = item;
            }
        }

        public void DeleteAt(int index)
        {
            // Если бы хранились в БД, то нужно было бы удалять по ID.
            if (index < 0 && index >= _games.Count) throw new IndexOutOfRangeException(nameof(index));

            if (_games[index].Removable)
                _games.RemoveAt(index);
        }

